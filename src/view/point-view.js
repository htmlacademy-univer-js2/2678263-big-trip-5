import AbstractView from '../framework/view/abstract-view.js';

import {getFormatDate, getFormatTime, getDuration} from '../utils/utils.js';

function createPointTemplate(point) {
  const {
    type,
    destinationName,
    dateFrom,
    dateTo,
    basePrice,
    resolvedOffers = [],
    isFavorite
  } = point;

  const dateStr = getFormatDate(dateFrom);
  const timeStart = getFormatTime(dateFrom);
  const timeEnd = getFormatTime(dateTo);
  const duration = getDuration(dateFrom, dateTo);

  const typeIcon = `img/icons/${type.toLowerCase()}.png`;
  const title = `${type.charAt(0).toUpperCase() + type.slice(1)} ${destinationName}`;

  const offersList = resolvedOffers.map((offer) => `
    <li class="event__offer">
      <span class="event__offer-title">${offer.title}</span>
      &plus;&euro;&nbsp;
      <span class="event__offer-price">${offer.price}</span>
    </li>
  `).join('');

  const favoriteBtnClass = isFavorite
    ? 'event__favorite-btn event__favorite-btn--active'
    : 'event__favorite-btn';

  return `
    <li class="trip-events__item">
      <div class="event">
        <time class="event__date" datetime="${dateFrom.split('T')[0]}">${dateStr}</time>
        <div class="event__type">
          <img class="event__type-icon" width="42" height="42" src="${typeIcon}" alt="Event type icon">
        </div>
        <h3 class="event__title">${title}</h3>
        <div class="event__schedule">
          <p class="event__time">
            <time class="event__start-time" datetime="${dateFrom}">${timeStart}</time>
            &mdash;
            <time class="event__end-time" datetime="${dateTo}">${timeEnd}</time>
          </p>
          <p class="event__duration">${duration}</p>
        </div>
        <p class="event__price">
          &euro;&nbsp;<span class="event__price-value">${basePrice}</span>
        </p>
        <h4 class="visually-hidden">Offers:</h4>
        <ul class="event__selected-offers">
          ${offersList}
        </ul>
        <button class="${favoriteBtnClass}" type="button">
          <span class="visually-hidden">Add to favorite</span>
          <svg class="event__favorite-icon" width="28" height="28" viewBox="0 0 28 28">
            <path d="M14 21l-8.22899 4.3262 1.57159-9.1631L.685209 9.67376 9.8855 8.33688 14 0l4.1145 8.33688 9.2003 1.33688-6.6574 6.48934 1.5716 9.1631L14 21z"/>
          </svg>
        </button>
        <button class="event__rollup-btn" type="button">
          <span class="visually-hidden">Open event</span>
        </button>
      </div>
    </li>
  `;
}

export default class PointView extends AbstractView {

  #point = null;
  #onOpenEditButtonClick = null;
  #handleFavoriteClick = null;

  constructor({point, onEditClick, onFavoriteClick}) {
    super();
    this.#point = point;
    this.#onOpenEditButtonClick = onEditClick;
    this.#setEventListeners();
    this.#handleFavoriteClick = onFavoriteClick;

    this.element.querySelector('.event__favorite-btn')
      .addEventListener('click', this.#favoriteClickHandler);
  }

  get template() {
    return createPointTemplate(this.#point);
  }

  #setEventListeners() {
    this.element.querySelector('.event__rollup-btn').addEventListener('click', this.#onOpenEditButtonClickHandler);
  }

  #onOpenEditButtonClickHandler = (evt) => {
    evt.preventDefault();
    this.#onOpenEditButtonClick();
  };

  #favoriteClickHandler = (evt) => {
    evt.preventDefault();
    this.#handleFavoriteClick();
  };

}


